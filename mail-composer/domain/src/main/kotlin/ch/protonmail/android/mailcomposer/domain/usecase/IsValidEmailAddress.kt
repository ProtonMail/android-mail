package ch.protonmail.android.mailcomposer.domain.usecase

import javax.inject.Inject

@Deprecated("Part of Composer V1, to be replaced with EmailValidator")
class IsValidEmailAddress @Inject constructor() {

    operator fun invoke(emailAddress: String): Boolean {
        val regex = EMAIL_VALIDATION_PATTERN.toRegex(RegexOption.IGNORE_CASE)
        return emailAddress.isNotBlank() && regex.matches(emailAddress)
    }

    private companion object {

        // Taken from core, apparently valid for RFC 5322. Does not impose maximum length restrictions, we will
        // rely in the API to give us an error in that case.
        @Suppress("MaxLineLength")
        const val EMAIL_VALIDATION_PATTERN =
            """(?:[a-z0-9!#${'$'}%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#${'$'}%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])"""
    }
}
